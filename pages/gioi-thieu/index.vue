    onMounted(() => {
      const animateUpsEl = document.querySelectorAll(".animate-about");

      animateUpsEl.forEach((el) => {
        const upsEl = el.querySelectorAll(".animate-about__item");
        useGsap.fromTo(
          upsEl,
          {
            y: 100,
            opacity: 0,
          },
          {
            y: 0,
            opacity: 1,
